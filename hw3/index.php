<h2>О НАС</h2>
<p>Основано в 2004 году как ТКО «Печатник». В конце 1990-х Андрей Герцев задумал открыть собственную книгоиздательскую фирму. К этой идее он пришёл в процессе собирания собственной библиотеки. Андрей занялся изучением издательского бизнеса. Несколько лет по выходным он уезжал в Ригу и Вильнюс на собрания Клуба книголюбов. В 2004 году Андрей Герцев, Сергей и Татьяна Деревянко открыли ТКО (творческое кооперативное объединение) «Печатник». Также в работе кооператива принял участие Яков Хелемский. Поначалу фирма занималась торговлей, а издавать книги начала с 2005 года. Одними из первых в «Печатнике» вышли романы Роберта Ладлэма, Александра Дюма, Гарольда Роббинса, а также книги по мотивам сериалов «Богатые тоже плачут» и «Рабыня Изаура». В 2006 году по решению учредителей ТКО «Печатник» разделилось на две части: фирму «Печатник» возглавил Андрей Герцев, а в «Печатник-Пресс» ушли Сергей и Татьяна Деревянко.</p>

<p>В 2005 году Андрей Герцев стал учредителем издательства «Астрель-СПб», которое в основном занимается художественной и детской литературой. Ежемесячно «Астрель-СПб» выпускает около 30 книг. «Астрель-СПб» является учредителем или соучредителем ряда литературных премий и конкурсов, в частности, литературной премии «Рукопись года». Премия вручается начинающим авторам, ещё не имеющим публикаций.

<p>К 2008 году «Печатник» стал одной из крупнейших издательских фирм России. В 2008 году «Печатник» приобрела «Аванту+» — лидера в сфере выпуска энциклопедий. Стоимость сделки оценивается в диапазоне от $4 млн до $10 млн.</p>
