<p>В группу Печатник до момента слияния с Издательской группой ЭКСМО входило более ста компаний, владельцем большей их части являлся кипрский офшор «Advanced Achievement Books Publishers Ltd.», а он, в свою очередь, принадлежит физическим лицам Андрею Герцеву, Якову Хелемскому (по 33,35 %), Олегу Бартеневу, Игорю Феоктистову (по 9,52 %), Юрию Хацкевичу (9,51 %) и Юрию Дейкало (4,75 %). Обороты компании не раскрываются, по оценкам конкурентов, выручка группы за июнь 2010 — июль 2011 года составила 5,7 млрд рублей. С начала июня 2012 года издательский бизнес «Печатник» выкупил Новиков Олег Евгеньевич, который в свою очередь является акционером и Президентом издательства «Эксмо». Объединение двух крупнейших издательств России привело к упрочнению их позиций на книжном рынке и позволило вести более масштабные совместные проекты, которые оказывают влияние на всю издательскую отрасль.</p>

<p>В мае 2010 года издательская группа «Печатник» приобрела издательства «Ультра. Культура» и «У-Фактория».</p>
